//  4 row 5 col

public class SquarePattern {
        public static void main(String arg[]) {
                for (int i = 0; i < 4; i++) {
                        for (int j = 0; j < 5; j++) {
                                System.out.print("*");
                        }
                        System.out.println();
                }
        }
}